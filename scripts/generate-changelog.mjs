// Auto-generates the Polaris changelog from Conventional Commit history.
//
// Runs on every build (locally and in CI — see package.json "build" and the
// deploy workflow's fetch-depth: 0). Reads commits made AFTER the last curated
// historical release and turns the user-facing ones into dated changelog
// entries, so the Changelog page always reflects what's actually shipped.
//
// Output: src/data/changelog-generated.ts (a typed array merged on top of the
// curated history in src/components/changelog-page.tsx).
//
// Commit prefix → entry type (everything else, incl. chore/docs/ci/build/test/
// style/revert, is ignored — those aren't user-facing):
//   feat:            → feature   (minor version bump)
//   feat!: / BREAKING → feature  (major version bump)
//   fix:             → fix       (patch bump)
//   perf: / refactor: → improvement (patch bump)

import { execSync } from 'node:child_process'
import { writeFileSync, mkdirSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { dirname, resolve } from 'node:path'

const __dirname = dirname(fileURLToPath(import.meta.url))
const OUT = resolve(__dirname, '../src/data/changelog-generated.ts')
const VERSION_OUT = resolve(__dirname, '../public/version.json')

// Build identity — served at /version.json and injected into the bundle as
// __BUILD_ID__ (see vite.config.ts). The DeployWatcher compares the two: when a
// new deploy changes /version.json but the running bundle still has the old id,
// it shows the "new version available" banner. Build id = commit sha + build time.
function writeVersion() {
  let sha = 'dev'
  try { sha = execSync('git rev-parse --short HEAD', { encoding: 'utf8' }).trim() || 'dev' } catch { /* no git */ }
  const builtAt = new Date().toISOString()
  const buildId = `${sha}-${Date.parse(builtAt)}`
  mkdirSync(dirname(VERSION_OUT), { recursive: true })
  writeFileSync(VERSION_OUT, JSON.stringify({ buildId, sha, builtAt }, null, 2) + '\n', 'utf8')
  console.log(`[version] wrote build id ${buildId} → ${VERSION_OUT}`)
}

// The newest CURATED release in changelog-page.tsx. Generated entries cover
// everything committed after it, versioned upward from here.
const BASE_VERSION = [2, 1, 1]
const SINCE = '2026-06-06' // day after the last curated release (v2.1.1, 2026-06-05)

const TYPE_FOR = (kind, breaking) => {
  if (breaking) return { type: 'feature', bump: 'major' }
  if (kind === 'feat') return { type: 'feature', bump: 'minor' }
  if (kind === 'fix') return { type: 'fix', bump: 'patch' }
  if (kind === 'perf' || kind === 'refactor') return { type: 'improvement', bump: 'patch' }
  return null // ignored
}

const SEP = '\x1f'

function readCommits() {
  try {
    const raw = execSync(
      `git log --no-merges --since="${SINCE}" --date=short --pretty=format:"%cd${SEP}%s"`,
      { encoding: 'utf8', maxBuffer: 16 * 1024 * 1024 },
    )
    return raw.split('\n').filter(Boolean)
  } catch {
    return [] // no git / shallow clone without history → emit empty (build still succeeds)
  }
}

const CONVENTIONAL = /^(\w+)(\([^)]*\))?(!)?:\s*(.+)$/

function cap(s) {
  return s.length ? s[0].toUpperCase() + s.slice(1) : s
}

function bumpVersion(v, bump) {
  const [maj, min, pat] = v
  if (bump === 'major') return [maj + 1, 0, 0]
  if (bump === 'minor') return [maj, min + 1, 0]
  return [maj, min, pat + 1]
}

function main() {
  const lines = readCommits()

  // Group user-facing commits by calendar date.
  const byDate = new Map() // date → { entries:[], bump }
  const rank = { patch: 0, minor: 1, major: 2 }

  for (const line of lines) {
    const idx = line.indexOf(SEP)
    if (idx === -1) continue
    const date = line.slice(0, idx)
    const subject = line.slice(idx + 1)
    const m = subject.match(CONVENTIONAL)
    if (!m) continue
    const kind = m[1].toLowerCase()
    const breaking = Boolean(m[3]) || /BREAKING CHANGE/.test(subject)
    const mapped = TYPE_FOR(kind, breaking)
    if (!mapped) continue

    if (!byDate.has(date)) byDate.set(date, { entries: [], bump: 'patch' })
    const group = byDate.get(date)
    const title = cap(m[4].trim())
    // De-dupe identical titles within a day.
    if (!group.entries.some((e) => e.title === title)) {
      group.entries.push({ type: mapped.type, title })
    }
    if (rank[mapped.bump] > rank[group.bump]) group.bump = mapped.bump
  }

  // Oldest → newest so version numbers accumulate deterministically.
  const datesAsc = [...byDate.keys()].sort()
  let version = [...BASE_VERSION]
  const releases = []
  for (const date of datesAsc) {
    const group = byDate.get(date)
    version = bumpVersion(version, group.bump)
    releases.push({
      version: version.join('.'),
      date,
      entries: group.entries,
    })
  }
  releases.reverse() // newest first for display

  const header =
    '// AUTO-GENERATED by scripts/generate-changelog.mjs — do not edit by hand.\n' +
    '// Regenerated on every build from Conventional Commit history.\n\n' +
    'import type { Release } from "@/components/changelog-page";\n\n' +
    'export const GENERATED_RELEASES: Release[] = '
  const body = JSON.stringify(releases, null, 2)

  mkdirSync(dirname(OUT), { recursive: true })
  writeFileSync(OUT, header + body + ';\n', 'utf8')
  console.log(`[changelog] generated ${releases.length} release(s) from commits since ${SINCE} → ${OUT}`)

  writeVersion()
}

main()
